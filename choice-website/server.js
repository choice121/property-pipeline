// Choice Properties — Replit Static File Server
// Serves the static site and injects config.js from environment variables.
// Optimisations:
//   • Nav + footer injected server-side → eliminates 2 fetch() calls per page
//   • First-page property data pre-fetched and embedded in listings.html
//     as window.__INITIAL_LISTINGS__ → properties paint immediately on load
//   • Strong Cache-Control headers on static assets (CSS/JS/fonts/images)
//   • config.js served no-cache so env-var changes show instantly

const http = require('http');
const https = require('https');
const fs   = require('fs');
const path = require('path');
const PORT = parseInt(process.env.PORT || '5000', 10);
const ROOT = __dirname;

