// ── Nav + footer: read once at startup ────────────────────────────────────
let navHtml    = '';
let footerHtml = '';
try {
  navHtml    = fs.readFileSync(path.join(ROOT, 'components/nav.html'),    'utf8');
  footerHtml = fs.readFileSync(path.join(ROOT, 'components/footer.html'), 'utf8');
  console.log('✓ Nav + footer components loaded');
} catch (e) {
  console.warn('⚠ Could not read nav/footer components:', e.message);
}

// ── Property cache (server-side pre-fetch for listings.html) ──────────────
const PROPERTY_CACHE_TTL = 3 * 60 * 1000; // 3 minutes
let propertyCache     = null;
let propertyCacheTime = 0;

function fetchListingsFromSupabase() {
  const supabaseUrl = config.SUPABASE_URL;
  const anonKey     = config.SUPABASE_ANON_KEY;
  if (!supabaseUrl || !anonKey) return Promise.resolve(null);

  const PER_PAGE = 24;
  const apiPath  =
    '/rest/v1/properties' +
    '?select=*,landlords(contact_name,business_name,avatar_url,verified)' +
    '&status=eq.active' +
    '&order=created_at.desc' +
    `&limit=${PER_PAGE}` +
    '&offset=0';

  const urlObj  = new URL(supabaseUrl + apiPath);
  const options = {
    hostname: urlObj.hostname,
    path:     urlObj.pathname + urlObj.search,
    method:   'GET',
    headers: {
      'apikey':        anonKey,
      'Authorization': `Bearer ${anonKey}`,
      'Prefer':        'count=exact',
    },
    timeout: 8000,
  };

  return new Promise((resolve) => {
    const req = https.request(options, (res) => {
      let body = '';
      res.on('data', (chunk) => { body += chunk; });
      res.on('end', () => {
        // Supabase returns 206 Partial Content when Prefer: count=exact is used
        if (res.statusCode !== 200 && res.statusCode !== 206) {
          console.warn(`⚠ Property cache: Supabase returned HTTP ${res.statusCode} — ${body.slice(0, 200)}`);
          resolve(null); return;
        }
        try {
          const rows = JSON.parse(body);
          if (!Array.isArray(rows)) { console.warn('⚠ Property cache: unexpected response shape'); resolve(null); return; }
          let total = rows.length;
          const rangeHeader = res.headers['content-range'];
          if (rangeHeader) {
            const m = rangeHeader.match(/\/(\d+)/);
            if (m) total = parseInt(m[1], 10);
          }
          resolve({
            rows,
            total,
            page:        1,
            per_page:    PER_PAGE,
            total_pages: Math.ceil(total / PER_PAGE),
          });
        } catch (e) {
          console.warn('⚠ Property cache: parse error —', e.message);
          resolve(null);
        }
      });
    });
    req.on('error', (e) => { console.warn('⚠ Property cache: network error —', e.message); resolve(null); });
    req.on('timeout', () => { req.destroy(); console.warn('⚠ Property cache: timed out'); });
    req.end();
  });
}

async function refreshPropertyCache() {
  const data = await fetchListingsFromSupabase();
  if (data) {
    propertyCache     = data;
    propertyCacheTime = Date.now();
    console.log(`✓ Property cache refreshed — ${data.rows.length} listings (total: ${data.total})`);
  } else {
    console.warn('⚠ Property cache fetch failed — serving without pre-loaded data');
  }
}

// Warm the cache at startup, then refresh every 3 minutes
refreshPropertyCache();
setInterval(refreshPropertyCache, PROPERTY_CACHE_TTL);

// ── HTML injection helper ─────────────────────────────────────────────────
// Injects nav+footer into every HTML response.
// Optionally embeds initial property data for listings.html.
function injectIntoHtml(html, filePath) {
  // Inject nav
  if (navHtml) {
    html = html.replace(/<div\s+id="site-nav"\s*><\/div>/g,
      `<div id="site-nav" data-server-injected="1">${navHtml}</div>`);
  }
  // Inject footer
  if (footerHtml) {
    html = html.replace(/<div\s+id="site-footer"\s*><\/div>/g,
      `<div id="site-footer" data-server-injected="1">${footerHtml}</div>`);
  }
  // Embed initial property data for listings.html only
  const isListings = filePath.endsWith('listings.html');
  if (isListings && propertyCache) {
    // Strip any build-time snapshot baked in by generate-config.js so we don't double-inject
    html = html.replace(/<script[^>]*>window\.__INITIAL_LISTINGS__[\s\S]*?<\/script>\n?/g, '');
    const dataScript =
      `<script>window.__INITIAL_LISTINGS__=${JSON.stringify(propertyCache)};` +
      `window.__INITIAL_LISTINGS_TS__=${propertyCacheTime};</script>`;
    html = html.replace('</head>', dataScript + '\n</head>');
  }
  return html;
}

// ── MIME types ────────────────────────────────────────────────────────────
const MIME = {
  '.html': 'text/html; charset=utf-8',
  '.css':  'text/css; charset=utf-8',
  '.js':   'application/javascript; charset=utf-8',
  '.json': 'application/json; charset=utf-8',
  '.svg':  'image/svg+xml',
  '.png':  'image/png',
  '.jpg':  'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.webp': 'image/webp',
  '.ico':  'image/x-icon',
  '.woff': 'font/woff',
  '.woff2':'font/woff2',
  '.ttf':  'font/ttf',
  '.txt':  'text/plain; charset=utf-8',
  '.xml':  'application/xml; charset=utf-8',
  '.webmanifest': 'application/manifest+json',
  '.zip':  'application/zip',
};

// Cache-Control headers per asset type
// HTML: no-cache (always revalidate so injected data is fresh)
// Static assets: immutable for 1 year (content-hashed via ?v= params)
function cacheHeader(ext) {
  if (ext === '.html')               return 'no-cache';
  if (['.css','.js'].includes(ext))  return 'public, max-age=31536000, immutable';
  if (['.woff','.woff2','.ttf','.svg','.png','.jpg','.jpeg','.webp','.ico'].includes(ext))
                                     return 'public, max-age=31536000, immutable';
  return 'public, max-age=3600';
}

// ── File server ───────────────────────────────────────────────────────────
function serveFile(res, filePath, statusCode) {
  const ext  = path.extname(filePath).toLowerCase();
  const mime = MIME[ext] || 'application/octet-stream';
  try {
    if (ext === '.html') {
      let html = fs.readFileSync(filePath, 'utf8');
      html = injectIntoHtml(html, filePath);
      res.writeHead(statusCode || 200, {
        'Content-Type':  mime,
        'Cache-Control': cacheHeader(ext),
      });
      res.end(html);
    } else {
      const data = fs.readFileSync(filePath);
      res.writeHead(statusCode || 200, {
        'Content-Type':  mime,
        'Cache-Control': cacheHeader(ext),
      });
      res.end(data);
    }
  } catch {
    res.writeHead(404, { 'Content-Type': 'text/html' });
    const notFound = path.join(ROOT, '404.html');
    res.end(fs.existsSync(notFound) ? fs.readFileSync(notFound) : '<h1>404 Not Found</h1>');
  }
}

// ── HTTP server ───────────────────────────────────────────────────────────
const server = http.createServer((req, res) => {
  let urlPath = req.url.split('?')[0];

  // Serve dynamically generated config.js (always no-cache)
  if (urlPath === '/config.js') {
    res.writeHead(200, {
      'Content-Type':  'application/javascript; charset=utf-8',
      'Cache-Control': 'no-store',
    });
    return res.end(configJs);
  }

  // Redirect browser-auto-requested icons to the SVG favicon
  if (
    urlPath === '/favicon.ico' ||
    urlPath === '/apple-touch-icon.png' ||
    urlPath === '/apple-touch-icon-precomposed.png'
  ) {
    res.writeHead(302, { 'Location': '/assets/favicon.svg' });
    return res.end();
  }

  // Resolve file path
  let filePath = path.join(ROOT, urlPath);

  // Security: prevent path traversal outside ROOT
  if (!filePath.startsWith(ROOT)) {
    res.writeHead(403);
    return res.end('Forbidden');
  }

  // Directory → index.html
  if (fs.existsSync(filePath) && fs.statSync(filePath).isDirectory()) {
    filePath = path.join(filePath, 'index.html');
  }

  // No extension → try .html
  if (!fs.existsSync(filePath) && !path.extname(urlPath)) {
    const withHtml = filePath + '.html';
    if (fs.existsSync(withHtml)) filePath = withHtml;
  }

  if (!fs.existsSync(filePath)) {
    const notFound = path.join(ROOT, '404.html');
    res.writeHead(404, { 'Content-Type': 'text/html; charset=utf-8' });
    return res.end(fs.existsSync(notFound) ? fs.readFileSync(notFound) : '<h1>404 Not Found</h1>');
  }

  serveFile(res, filePath);
});

server.listen(PORT, '0.0.0.0', () => {
  console.log(`Choice Properties server running on port ${PORT}`);
  console.log(`SUPABASE_URL:      ${config.SUPABASE_URL      ? '✓ set' : '✗ missing'}`);
  console.log(`SUPABASE_ANON_KEY: ${config.SUPABASE_ANON_KEY ? '✓ set' : '✗ missing'}`);
  console.log(`IMAGEKIT_URL:      ${config.IMAGEKIT_URL      ? '✓ set' : '✗ missing'}`);
  console.log(`GEOAPIFY_API_KEY:  ${config.GEOAPIFY_API_KEY  ? '✓ set' : '✗ missing'}`);
});
